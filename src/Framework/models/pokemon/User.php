<?php

declare(strict_types=1);

namespace Acme\Framework\models;

use DateTime;

abstract class User
{
    public function __construct(
        protected int $id,
        protected string $user_typpe,
        protected string $password,
        protected DateTime $created_at,
        protected DateTime $updated_at
    ) {
    }

    /**
     * Get the value of id
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set the value of id
     *
     * @return  self
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Get the value of user_typpe
     */
    public function getUser_typpe()
    {
        return $this->user_typpe;
    }

    /**
     * Set the value of user_typpe
     *
     * @return  self
     */
    public function setUser_typpe($user_typpe)
    {
        $this->user_typpe = $user_typpe;

        return $this;
    }

    /**
     * Get the value of password
     */
    public function getPassword()
    {
        return $this->password;
    }

    /**
     * Set the value of password
     *
     * @return  self
     */
    public function setPassword($password)
    {
        $this->password = $password;

        return $this;
    }

    /**
     * Get the value of created_at
     */
    public function getCreated_at()
    {
        return $this->created_at;
    }

    /**
     * Set the value of created_at
     *
     * @return  self
     */
    public function setCreated_at($created_at)
    {
        $this->created_at = $created_at;

        return $this;
    }

    /**
     * Get the value of updated_at
     */
    public function getUpdated_at()
    {
        return $this->updated_at;
    }

    /**
     * Set the value of updated_at
     *
     * @return  self
     */
    public function setUpdated_at($updated_at)
    {
        $this->updated_at = $updated_at;

        return $this;
    }
}
